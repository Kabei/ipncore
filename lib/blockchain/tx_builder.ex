  def send(
        channel,
        pubkey,
        secret_key,
        from_address,
        to_address,
        token,
        amount,
        pool,
        data_type \\ "json"
      ) do
    timestamp = Chain.get_time()
    diff_ipn = @token != token

    fee = Tx.calc_fees(amount, pool.fee, pool.percent, 0)

    base_outputs = [
      %{
        address: to_address,
        tid: token,
        type: @output_type_send,
        value: amount
      },
      %{
        address: pool.address,
        tid: @token,
        type: @output_type_fee,
        value: fee
      }
    ]

    {inputs, outputs} =
      if diff_ipn do
        {inputs, ret_output, _total_value} = get_inputs!(from_address, token, amount, channel)

        {inputs2, ret_output2, _total_value} = get_inputs!(from_address, @token, amount, channel)

        {inputs ++ inputs2, base_outputs ++ ret_output ++ ret_output2}
      else
        {inputs, ret_output, _total_value} = get_inputs!(from_address, token, amount, channel)

        {inputs, base_outputs ++ ret_output}
      end

    pre_tx = %{
      channel: channel,
      outputs: txo,
      time: timestamp,
      token: token,
      type: type,
      version: 0
    }

    hash = Tx.compute_hash(pre_tx)
    IO.inspect("hash #{Base.encode16(hash)}")
    {:ok, signature} = Falcon.sign(secret_key, hash)

    pre_tx
    |> Map.put(:outputs, outputs_base58(txo))
    |> Map.put(:sigs, [
      %{
        address: [Base58Check.encode(from_address)],
        pubkey: Base.encode64(pubkey),
        sig: Base.encode64(signature)
      }
    ])
    |> io_puts(data_type)
  end

  defp io_puts(tx_struct, data_type) do
    case data_type do
      "json" ->
        tx_struct
        |> Jason.encode!()
        |> IO.puts()

      _ ->
        for {k, v} <- tx_struct, into: %{} do
          cond do
            is_list(v) ->
              {to_string(k),
               for a_map <- v do
                 map_string_keys(a_map)
               end}

            true ->
              {to_string(k), v}
          end
        end
    end
  end

  defp map_string_keys(obj) when is_map(obj) do
    for {k, v} <- obj, into: %{} do
      {to_string(k), v}
    end
  end

  defp map_string_keys(obj), do: obj

  defp get_inputs!(from_address, token, req_amount, channel) do
    %{
      balance: _balance,
      utxo: utxo
    } = get_utxo!([from_address], token, req_amount, channel)

    {inputs, ret_output, total_value} =
      Enum.reduce_while(utxo, {[], %{}, 0}, fn x, {acc_inputs, acc_outputs, acc} ->
        total = acc + x.value
        acc_inputs = acc_inputs ++ [x.id]

        cond do
          total == req_amount ->
            {:halt, {acc_inputs, acc_outputs, total}}

          total > req_amount ->
            {:halt,
             {acc_inputs,
              %{
                address: from_address,
                tid: x.tid,
                type: @output_type_return,
                value: total - req_amount
              }, total}}

          true ->
            {:cont, {acc_inputs, acc_outputs, total}}
        end
      end)

    # if total_value <= amount, do: throw(:insuficient_found)
    {inputs, ret_output, total_value}
  end

  defp get_utxo!(addresses, token, req_amount, channel) do
    IO.inspect("get_utxo")
    IO.inspect(addresses)
    IO.inspect(token)
    IO.inspect(req_amount)
    result = Utxo.get_by_address(addresses, token, req_amount, channel)

    if result == [] do
      throw(:not_found)
    end

    %{
      balance: balance,
      utxo: _utxo
    } = result

    if balance < req_amount do
      IO.inspect("balance: #{balance} | req: #{req_amount}")
      throw(:insuficient_found)
    end

    result
  end

  defp outputs_base58(x) do
    Enum.map(x, fn %{address: address, tid: tid, value: value, type: type} ->
      %{address: Base58Check.encode(address), tid: tid, type: type, value: value}
    end)
  end

  defp inputs_base62(x) do
    Enum.map(x, &Base62.encode(&1))
  end
